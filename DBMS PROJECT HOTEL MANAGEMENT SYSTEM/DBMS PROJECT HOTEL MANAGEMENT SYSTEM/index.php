             <h2>Infrastructure</h2>
                <p>
                    There are ten buildings in the hostel campus. Boys blocks are named as <strong>Purandar, Shivneri, Panhalgad, Torna, Raigad, Pratapgad, Sajjangadand Vishalgad</strong>.The hostel campus houses about 1064 boys students.<br>&emsp;&emsp;Further it comprises of hostel office, security room, guest house of three rooms, Nine rectors quarters and Director's residence. Student messes are run on co-operative basis.
                </p>
                <hr>
                <h2>Amenities</h2>
                <p>
                    The students are provided with basic amenities like <strong>cot, table, chair, cupboard</strong> in their rooms. The rooms are spacious, airy, and illuminated with the surroundings landscaped with greenery.<br>&emsp;&emsp; Students common rooms have TV sets and carrom. The water supply is for 24 hours. A generator ensures electric power supply to messes and common rooms.The college gymkhana ground (about 6.5 acres) is near the hostel for playing <strong>cricket, hockey, volleyball, football, basketball, tennis and jogging</strong>. The basket ball and tennis courts are floodlit allowing students to play late in the evening. 
                    <br><br>&emsp;&emsp;There is a computer room with 100 machines for both boys with <strong>48 mbps</strong> connectivity. Internet connectivity is also provided in every room. The different operating systems and platforms are made available in computer room. <br>&emsp;&emsp;
                    The hostel campus maintenance like cleaning /sweeping, pest control is out sourced. The electrical repairs, and security services are round the clock.
                </p>
